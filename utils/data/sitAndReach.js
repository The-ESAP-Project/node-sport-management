/**
 * 计算坐位体前屈得分
 * @param {number} distance - 坐位体前屈距离，单位：厘米
 * @param {string} gender - 性别，'male'或'female'
 * @param {number} grade - 年级，1表示高一，2表示高二，3表示高三
 * @returns {Object} example: { distance: 19.4, score: 90, level: "良好" }
 */
function calculateSitAndReachScore(distance, gender = 'male', grade = 1) {
    // 参数验证
    if (!['male', 'female'].includes(gender)) {
        throw new Error('性别参数必须为"male"或"female"');
    }
    
    if (![1, 2, 3].includes(grade)) {
        throw new Error('年级参数必须为1、2或3，分别代表高一、高二、高三');
    }
    
    // 获取对应性别和年级的坐位体前屈标准
    const gradeKey = `grade${grade}`;
    const standards = sitAndReachStandard[gender][gradeKey];
    
    // 计算得分
    let score = 0;
    let level = '';
    
    for (let i = 0; i < standards.length - 1; i++) {
        if (distance >= standards[i].value) {
            score = standards[i].score;
            break;
        }
    }
    
    // 距离小于所有标），则给最低分
    if (score === 0 && standards.length > 0) {
        score = standards[standards.length - 1].score;
    }
    
    // 确定等级
    if (score >= sitAndReachStandard.levels.excellent.min) {
        level = sitAndReachStandard.levels.excellent.desc;
    } else if (score >= sitAndReachStandard.levels.good.min) {
        level = sitAndReachStandard.levels.good.desc;
    } else if (score >= sitAndReachStandard.levels.pass.min) {
        level = sitAndReachStandard.levels.pass.desc;
    } else {
        level = sitAndReachStandard.levels.fail.desc;
    }
    
    return {
        distance,
        score,
        level
    };
}


